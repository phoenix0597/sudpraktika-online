# Парсит "фраза:кол-во | фраза:кол-во" → список (фраза, кол-во)
_PAIR_RE = re.compile(r"^(.*?):(\d+)\s*$")


def parse_cluster_demand(top_phrases: str) -> int:
    """Сумма частот всех фраз в кластере (long-tail), иначе 0."""
    if not top_phrases:
        return 0
    total = 0
    for chunk in top_phrases.split("|"):
        m = _PAIR_RE.match(chunk.strip())
        if m:
            total += int(m.group(2))
    return total


def main() -> int:
    ap = argparse.ArgumentParser(description="Агрегация спроса из результатов Wordstat")
    ap.add_argument("--input", required=True, help="CSV из wordstat_fetch.py")
    ap.add_argument("--output", default="", help="Опц.: CSV со сводкой по вертикалям")
    args = ap.parse_args()

    rows = []
    with Path(args.input).open("r", encoding="utf-8", newline="") as f:
        for row in csv.DictReader(f):
            phrase = row["phrase"].strip()
            cluster = parse_cluster_demand(row.get("top_phrases", ""))
            seed = int(row.get("total_shows") or 0)
            # Спрос кластера = сумма long-tail; если пусто — берём seed как оценку
            demand = cluster if cluster else seed
            vertical = VERTICAL_MAP.get(phrase, "Прочее")
            rows.append((phrase, vertical, seed, cluster, demand))

    # Сводка по вертикалям
    by_vert = defaultdict(lambda: {"demand": 0, "clusters": 0})
    for phrase, vert, seed, cluster, demand in rows:
        by_vert[vert]["demand"] += demand
        by_vert[vert]["clusters"] += 1

    print(f"{'Вертикаль':<24} {'Кластеры':>8} {'Спрос/мес':>12}")
    print("-" * 48)
    total_demand = 0
    total_clusters = 0
    for vert, d in sorted(by_vert.items(), key=lambda x: -x[1]["demand"]):
        print(f"{vert:<24} {d['clusters']:>8} {d['demand']:>12,}")
        total_demand += d["demand"]
        total_clusters += d["clusters"]
    print("-" * 48)
    print(f"{'ИТОГО':<24} {total_clusters:>8} {total_demand:>12,}")
    print(f"\nСредний спрос на кластер: {total_demand // max(total_clusters, 1):,}")

    # Топ-10 кластеров по спросу
    print(f"\nТоп-10 кластеров по спросу (с учётом long-tail):")
    for phrase, vert, seed, cluster, demand in sorted(rows, key=lambda r: -r[4])[:10]:
        print(f"  {demand:>8,}  [{vert}]  {phrase}")

    if args.output:
        with Path(args.output).open("w", encoding="utf-8", newline="") as f:
            w = csv.writer(f)
            w.writerow(["vertical", "clusters", "demand_per_month"])
            for vert, d in sorted(by_vert.items(), key=lambda x: -x[1]["demand"]):
                w.writerow([vert, d["clusters"], d["demand"]])
        print(f"\nСводка записана: {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
